function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/** @returns {string} PascalCase nickname, max 32 chars */
export function generateOperatorNickname() {
  const second = Math.random() < 0.5 ? pick(NOUNS) : pick(VERBS);
  let s = `${pick(ADJECTIVES)}${second}`;
  if (s.length > 32) s = s.slice(0, 32);
  return s;
}
